class Wall {
  constructor(props) {
    this.position = props.position;
    this.size = props.size;
    this.height = props.height;
  }

  render() {
    const wall = document.createElement('a-box');
    wall.setAttribute('color', '#fff');
    wall.setAttribute('material', `src: #texture-wall;`);
    wall.setAttribute('width', this.size);
    wall.setAttribute('height', this.height);
    wall.setAttribute('depth', this.size);
    wall.setAttribute('position', this.position);
    wall.setAttribute('static-body', '');

    return wall;
  }
}

const buildWalls = () => {
  var WALL_SIZE = 3,
      WALL_HEIGHT = 6;
  var el = document.querySelector('#walls');

  map.data.forEach((row, y) => {
    row.forEach((tile, x) => {
      const xPosition = (x - map.width / 2) * WALL_SIZE;
      const yPosition = (y - map.height / 2) * WALL_SIZE;
      const zPosition = WALL_HEIGHT / 2;

      if (tile === 1) {
          // Create wall
          el.appendChild(
            new Wall({
              size: WALL_SIZE,
              height: WALL_HEIGHT,
              position: `${xPosition} ${zPosition} ${yPosition}`
            }).render()
          );
      }

      if (tile === 2)  {
          // Set player position
          document.querySelector('#player').setAttribute('position', `${xPosition} 1.7 ${yPosition}`);
      }
    });
  });

  console.info('Walls added.');
}

const scene = document.querySelector('a-scene');
if (scene.hasLoaded) {
  buildWalls();
} else {
  scene.addEventListener('loaded', buildWalls);
}


